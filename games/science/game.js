GameRegistry.register({
  id: 'science',
  name: '科学知识',
  icon: '🔬',
  desc: '小学到初中科学知识问答，生物物理化学全覆盖！',
  difficulties: [
    { id: 'easy', name: '小学科学', icon: '🌱', desc: '动植物、人体、天气等基础科学' },
    { id: 'medium', name: '初中基础', icon: '🔥', desc: '生物物理化学入门知识' },
    { id: 'hard', name: '中考核心', icon: '💀', desc: '中考高频考点与实验题' },
  ],

  init(container, difficulty, onComplete) {
    const pool = SCIENCE_QS[difficulty];
    const total = 5;
    const shuffled = [...pool].sort(() => Math.random() - 0.5).slice(0, total);
    let index = 0, correct = 0;

    showQ();
    function showQ() {
      if (index >= total) { finish(); return; }
      const q = shuffled[index];
      container.innerHTML = `<div style="max-width:500px;width:100%;" class="animate-in">
        <div style="color:var(--text-dim);margin-bottom:12px;">第 ${index+1}/${total} 题 | ✅ ${correct}</div>
        <div style="font-size:1.15em;font-weight:600;margin-bottom:20px;line-height:1.7;background:var(--surface);padding:20px;border-radius:var(--radius);">🔬 ${q.q}</div>
        <div class="flex-col" id="opts">${q.o.map((opt,i)=>`<button class="btn btn-secondary" data-idx="${i}" style="text-align:left;width:100%;">${String.fromCharCode(65+i)}. ${opt}</button>`).join('')}</div>
        <div id="fb" style="margin-top:10px;min-height:28px;font-weight:700;"></div></div>`;
      document.getElementById('opts').onclick=(e)=>{const b=e.target.closest('[data-idx]');if(!b)return;const c=+b.dataset.idx;const f=document.getElementById('fb');if(c===q.a){correct++;f.innerHTML='<div class="game-feedback success pop">✅ 正确！</div>';}else{f.innerHTML='<div class="game-feedback info">❌ 正确答案：<b>'+q.o[q.a]+'</b></div>';}index++;setTimeout(showQ,1000);};
    }
    function finish(){
      const star=correct===5?'⭐⭐⭐⭐⭐':correct>=4?'⭐⭐⭐⭐':correct>=3?'⭐⭐⭐':'⭐';
      container.innerHTML=`<div class="result-card animate-in"><div class="result-icon">${correct>=4?'🎉':correct>=3?'👍':'📚'}</div><div class="result-title">${correct>=4?'科学小达人！':correct>=3?'还不错！':'继续学习！'}</div><div class="result-detail">${total}题答对<b style="color:var(--accent);">${correct}</b>题<br>${star}</div></div>`;
      setTimeout(()=>onComplete({win:correct>=3,score:total-correct,title:correct>=4?'太棒了！':'挑战完成！',detail:`${total}题答对${correct}题`}),1500);
    }
  }
});

